package org.siri_hate.main_service.model.dto.response.team;

public class TeamBaseResponse {

    private Long id;
    private String username;
    private String role;

    public TeamBaseResponse() {
    }

    public TeamBaseResponse(Long id, String username, String role) {
        this.id = id;
        this.username = username;
        this.role = role;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }
}
